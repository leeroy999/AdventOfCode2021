function parse_input(input)
    local arr = {}
    for line in string.gmatch(input, ".-\n") do
        line = string.gsub(line, "\n", "")
        arr[#arr + 1] = {}
        for char in string.gmatch(line, "%S") do
            arr[#arr][#arr[#arr] + 1] = char
        end
    end
    return arr
end

